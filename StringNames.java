public class StringNames {
	public static void main(String[] args) {
		String firstName = "Natelle", middleName = "Kate", lastName = "Ziegler";
		char firstInitial = 'N', middleInitial = 'K', lastInitial = 'Z';
		
		System.out.println("You are speaking to " + firstName + " " + middleName + " " + lastName);
		System.out.println("-  " + firstInitial + middleInitial + lastInitial);
	}
}