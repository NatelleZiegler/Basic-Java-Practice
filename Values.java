public class Values {
	public static void main(String[] args) {
		int value = 0;
		
		value = 6 + 3 * 5;
		System.out.println("The value is " + value);
		
		value = 12 / 2 - 4;
		System.out.println("The value is " + value);
		
		value = 9 + 14 * 2 - 6;
		System.out.println("The value is " + value);
		
		value = 5 + 19 % 3 - 1;
		System.out.println("The value is " + value);
		
		value = (6 + 2) * 3;
		System.out.println("The value is " + value);
		
		value = 14 / (11 - 4);
		System.out.println("The value is " + value);
		
		value = 9 + 12 * (8 - 3);
		System.out.println("The value is " + value);
		
		double portion;
		portion = 70/3;
		System.out.println("The value is " + portion);	
	}
}